export function DotMatrixDisplay({
     width = 600, height = 100, className, text = "PEAK HOLD", mode = 'TEXT' 
    }: DotMatrixDisplayProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null)
  const frameIndexRef = useRef(0)
  const lastFrameTimeRef = useRef(0)
  const ANIMATION_SPEED = 30 // ms per frame

  useEffect(() => {
    const canvas = canvasRef.current
    if (!canvas) return

    const ctx = canvas.getContext("2d")
    if (!ctx) return

    // ÊèèÁîª„É´„Éº„Éó
    let animationFrameId: number

    const render = (timestamp: number) => {
      // „Ç≠„É£„É≥„Éê„Çπ„Çí„ÇØ„É™„Ç¢
      ctx.clearRect(0, 0, canvas.width, canvas.height)

      // Ë®≠ÂÆö„ÅÆË™≠„ÅøËæº„Åø
      const {
        position,
        rows,
        cols,
        numCells,
        pixel,
        dotGapX,
        dotGapY,
        cellGap,
        color,
        offColor,
        glowBlur,
        debugAllOn,
      } = DOT_MATRIX_CONFIG

      if (mode === 'ANIMATION') {
        if (timestamp - lastFrameTimeRef.current > ANIMATION_SPEED) {
          frameIndexRef.current = (frameIndexRef.current + 1) % DOLPHIN_CUSTOM_FRAMES.length
          lastFrameTimeRef.current = timestamp
        }
      } else {
        frameIndexRef.current = 0 // Reset animation when in text mode
      }

      ctx.shadowBlur = glowBlur
      ctx.shadowColor = color

      const cellWidth = cols * (pixel.width + dotGapX) - dotGapX
      const totalHeight = rows * (pixel.height + dotGapY) - dotGapY
      
      // ÂÖ®„Çª„É´„ÇíÊèèÁîª
      for (let cellIndex = 0; cellIndex < numCells; cellIndex++) {
        const cellStartX = position.x + cellIndex * (cellWidth + cellGap)
        const cellStartY = position.y - totalHeight

        // „Çª„É´ÂÜÖ„ÅÆ„Éâ„ÉÉ„Éà„ÇíÊèèÁîª (5x7)
        for (let r = 0; r < rows; r++) {
          // ‰∏ã„Åã„Çâ„ÅÆË°å„Ç§„É≥„Éá„ÉÉ„ÇØ„ÇπÔºà0Âßã„Åæ„ÇäÔºâ„ÇíË®àÁÆó
          // r=0„Åå‰∏ÄÁï™‰∏ä„ÄÅr=rows-1„Åå‰∏ÄÁï™‰∏ã„Å™„ÅÆ„ÅßÂèçËª¢„Åï„Åõ„Çã
          const rowIndexFromBottom = rows - 1 - r
          
          // stackSlant„Å´„Çà„ÇãXÂ∫ßÊ®ô„ÅÆ„Ç∫„É¨„ÇíË®àÁÆóÔºà‰∏ä„Å´„ÅÑ„Åè„Åª„Å©Âè≥„Å´„Åö„Çå„ÇãÔºâ
          const stackShift = rowIndexFromBottom * pixel.stackSlant

          for (let c = 0; c < cols; c++) {
            const x = cellStartX + c * (pixel.width + dotGapX) + stackShift
            const y = cellStartY + r * (pixel.height + dotGapY)

            // ÁÇπÁÅØÁä∂ÊÖã„ÅÆÂà§ÂÆö
            let isOn = debugAllOn
            
            if (!isOn) {
              if (mode === 'TEXT' && text) {
                const char = text[cellIndex] || ' '
                const pattern = CHAR_PATTERNS[char.toUpperCase()] || CHAR_PATTERNS[' ']
                if (pattern && pattern[r] && pattern[r][c]) {
                  isOn = true
                }
              } else if (mode === 'ANIMATION') {
                const currentFrame = DOLPHIN_CUSTOM_FRAMES[frameIndexRef.current]
                if (currentFrame) {
                  // currentFrame[r] „ÅØ7Ë°å„ÅÆ„ÅÜ„Å°„ÅÆ1Ë°åÔºà12„Çª„É´ÂàÜ„ÅÆÈÖçÂàóÔºâ
                  // currentFrame[r][cellIndex] „ÅØË©≤ÂΩì„Çª„É´„ÅÆ5ÊñáÂ≠ó„ÅÆÊñáÂ≠óÂàó
                  const cellString = currentFrame[r][cellIndex]
                  if (cellString && cellString[c] === '1') {
                    isOn = true
                  }
                }
              }
            }

            ctx.fillStyle = isOn ? color : offColor
            // Ê∂àÁÅØ„Éâ„ÉÉ„Éà„ÅØÁô∫ÂÖâ„Åï„Åõ„Å™„ÅÑ
            ctx.shadowBlur = isOn ? glowBlur : 0

            // „ÉÄ„Éñ„É´„Çπ„É©„É≥„Éà„ÅÆÊèèÁîª„É≠„Ç∏„ÉÉ„ÇØ
            const drawX = x
            const drawY = y + pixel.height // Â∑¶‰∏ãÂ∫ßÊ®ô

            const p4 = { x: drawX, y: drawY } // Â∑¶‰∏ã
            const p3 = { x: drawX + pixel.width, y: drawY - pixel.slopeTB } // Âè≥‰∏ã
            const p1 = { x: drawX + pixel.slantLR, y: drawY - pixel.height } // Â∑¶‰∏ä
            const p2 = { x: drawX + pixel.width + pixel.slantLR, y: drawY - pixel.height - pixel.slopeTB } // Âè≥‰∏ä

            ctx.beginPath()
            ctx.moveTo(p1.x, p1.y)
            ctx.lineTo(p2.x, p2.y)
            ctx.lineTo(p3.x, p3.y)
            ctx.lineTo(p4.x, p4.y)
            ctx.closePath()
            ctx.fill()
          }
        }
      }

      animationFrameId = requestAnimationFrame(render)
    }

    render(0) // Pass initial timestamp

    return () => {
      cancelAnimationFrame(animationFrameId)
    }
  }, [mode, text]) // Re-run effect when mode or text changes

  return <canvas ref={canvasRef} width={width} height={height} className={className} />
}
